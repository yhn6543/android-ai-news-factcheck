package com.example.fakenews.data.model

data class FactCheckResult(
    val finalSummary: String,
    val riskLevel: FactCheckRiskLevel,
    val fakeNewsPossibility: Int,
    val logicAnalysis: String,
    val emotionalBiasAnalysis: String,
    val sourceReliabilityAnalysis: String,
    val exaggerationAnalysis: String,
    val factsVsOpinions: String,
    val additionalChecks: List<String>,
    val recommendedSearchKeywords: List<String>,
    val relatedArticleCount: Int,
    val relatedArticles: List<NewsArticle>,
    val verdict: FactCheckVerdict = FactCheckVerdict.UNVERIFIABLE,
    val confidenceScore: Int = 0,
    val reason: String = "",
    val evidenceSummary: String = "",
    val contradictingEvidence: List<String> = emptyList(),
    val missingContext: List<String> = emptyList(),
    val recommendedChecks: List<String> = emptyList(),
    val claimAnalysis: ClaimAnalysis? = null,
    val sourceCount: Int = relatedArticleCount,
    val sources: List<GroundedSource> = emptyList(),
    val searchQueriesUsed: List<String> = emptyList(),
    val usedInternalKnowledge: Boolean = false,
    val claimCategory: FactCheckClaimCategory = FactCheckClaimCategory.UNKNOWN,
    val timeSensitivity: FactCheckTimeSensitivity = FactCheckTimeSensitivity.UNKNOWN,
    val needsTimeContext: Boolean = false,
    val originalText: String = "",
    val rawSourceCount: Int = sourceCount,
    val uniqueSourceCount: Int = sourceCount,
    val uniqueUriSourceCount: Int = 0,
    val evidenceDirectness: EvidenceDirectness = EvidenceDirectness.NONE,
    val confidenceParseSuccess: Boolean = true,
    val rawConfidenceFromGemini: String = "",
    val rawConfidenceType: String = "",
    val ambiguousLowInteger: Boolean = false,
    val confidenceBeforePostProcess: Int = confidenceScore,
    val confidenceAfterPostProcess: Int = confidenceScore,
    val confidenceAdjustmentReason: String = ""
)
